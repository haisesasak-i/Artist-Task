public class Artist{
	private String name;
	
	public Artist(String name){
	
		this.name = name;
	
	}
	public String getName(){
	
		return this.name;
	
	}
	@Override
	public String toString (){
	
		return "The name of the artist is \" this.name \"";
	
	}
	public void setName(String name){
	  this.name = name;
	}















}
